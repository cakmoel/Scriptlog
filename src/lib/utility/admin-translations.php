/**
 * Admin Panel Translations
 *
 * This file provides translations for the admin panel.
 * It uses a hybrid approach:
 * 1. First tries to get translation from database (tbl_translations)
 * 2. Falls back to hardcoded array if not found in database
 *
 * Key format: dot-notation (e.g., 'nav.dashboard', 'form.save')
 *
 * Supported locales:
 * - en: English (default)
 * - ar: Arabic (RTL)
 * - zh: Chinese
 * - fr: French
 * - ru: Russian
 * - es: Spanish
 * - id: Indonesian
 */

function admin_translate(string $key, ?string $locale = null): string
{
    static $dbCache = [];
    static $fallbackLoaded = false;

    if ($locale === null) {
        $locale = admin_get_locale();
    }

    if (!isset($dbCache[$locale])) {
        $dbCache[$locale] = [];
        if (class_exists('TranslationDao')) {
            try {
                $translationDao = new TranslationDao();
                $translations = $translationDao->findByLocaleCode($locale);
                foreach ($translations as $t) {
                    $dbCache[$locale][$t['translation_key']] = $t['translation_value'];
                }
            } catch (Throwable $e) {
                // Database not available, use fallback only
            }
        }
    }

    if (isset($dbCache[$locale][$key])) {
        return $dbCache[$locale][$key];
    }

    if (!$fallbackLoaded) {
        $fallbackLoaded = true;
    }

    return admin_translate_fallback($key, $locale);
}

