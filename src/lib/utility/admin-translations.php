}

function admin_is_rtl(): bool
{
    return in_array(admin_get_locale(), ['ar']);
}

function admin_t(string $key, array $params = [], ?string $locale = null): string
{
    $value = admin_translate($key, $locale);

    if (!empty($params)) {
        foreach ($params as $k => $v) {
            $value = str_replace(':' . $k, $v, $value);
        }
    }

    return $value;
}
